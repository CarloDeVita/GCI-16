    public void filterList(){
        int v; //0 : paymentOrder is not filtered
        ((DefaultTableModel)poTable.getModel()).setRowCount(0);
        for(PaymentOrder p : list) {
            v = 1;
            for(String s : mapVal.keySet() ){
                if(s.equals("protocol") ){
                    String protocol = mapVal.get(s);
                    if( ! p.getProtocol().equals(Integer.parseInt(protocol))){
                        v = 0;
                    }
                }
                if(s.equals("debtor") ){
                    String debtor = mapVal.get(s);
                    if( ! p.getDebtor().equals(debtor)){
                        v = 0;
                    }
                }
            }
            if(v == 1){
                addPaymentOrderTable(p);
            }
        
        }
    }

    
    private void addPaymentOrderTable(PaymentOrder p){
        Object[] values = new Object[6];
        values[0] = p.getProtocol();
        values[1] = p.getDebtor();
        values[2] = p.getYear();
        values[3] = p.getTrimester();
        values[4] = p.getAmount();
        values[5] = p.getStatus();
        ((DefaultTableModel)poTable.getModel()).addRow(values);
    }
    
    public void addPaymentOrder(PaymentOrder p){
        list.add(p);
        this.addPaymentOrderTable(p);
        
    }
    
    public void setTable(){
        HttpURLConnection conn = ServerConnection.executeGet("http://localhost:8081/GCI16/PaymentOrder?action=show");
   
        
        try {
            InputStream is = conn.getInputStream();
            BufferedReader rd = new BufferedReader(new InputStreamReader(is));
            String line;
            line = rd.readLine();
            rd.close();
            
            Gson gson = new Gson();
            java.lang.reflect.Type POListType = new TypeToken<Collection< PaymentOrder> >(){}.getType();
            list = gson.fromJson(line, POListType);
            
            Object[] values = new Object[6];
            for(PaymentOrder p : list){
                this.addPaymentOrderTable(p);
            }
            
        } catch (IOException ex) {
            Logger.getLogger(PaymentOrderTable.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
